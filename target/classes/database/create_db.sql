CREATE TABLE USUARIO (
    id BIGINT NOT NULL AUTO_INCREMENT,
    nombre VARCHAR(45) NOT NULL,
    edad INT NOT NULL,
    email VARCHAR(100) NOT NULL ,
    password VARCHAR(100) NOT NULL,
    PRIMARY KEY (id)
);

CREATE TABLE RECETAS (
    ID BIGINT NOT NULL AUTO_INCREMENT,
    nombre VARCHAR(45) NOT NULL,
    duracion INT NOT NULL,
    usuario_id BIGINT NOT NULL,
    PRIMARY KEY (ID)
);

ALTER TABLE RECETAS ADD CONSTRAINT rece_usua FOREIGN KEY (usuario_id) REFERENCES USUARIO(ID);

CREATE TABLE INGREDIENTES (
    ID BIGINT NOT NULL AUTO_INCREMENT,
    nombre VARCHAR(45) NOT NULL,
    cantidad INT NOT NULL,
    unidad VARCHAR(45) NOT NULL,
    receta_id BIGINT NOT NULL,
    PRIMARY KEY (ID)
  );

ALTER TABLE INGREDIENTES ADD CONSTRAINT ingr_rece FOREIGN KEY (receta_id) REFERENCES RECETAS(ID);

CREATE TABLE PASOS (
    ID BIGINT NOT NULL AUTO_INCREMENT,
    instrucciones VARCHAR(200) NOT NULL,
    orden INT NOT NULL,
    receta_id BIGINT NOT NULL,
    PRIMARY KEY (ID)
);

ALTER TABLE PASOS  ADD CONSTRAINT paso_rece FOREIGN KEY (receta_id)  REFERENCES RECETAS(ID);

CREATE TABLE COMENTARIOS (
    ID BIGINT NOT NULL AUTO_INCREMENT,
    resena VARCHAR(200) NOT NULL,
    puntuacion INT NOT NULL,
    usuario_id BIGINT NOT NULL,
    receta_id BIGINT NOT NULL,
    PRIMARY KEY (ID)
);

ALTER TABLE COMENTARIOS ADD CONSTRAINT comen_rece FOREIGN KEY (receta_id) REFERENCES RECETAS(ID);

CREATE TABLE CARPETA (
    ID BIGINT NOT NULL, 
    guardado INT NOT NULL,
    usuario_id BIGINT NOT NULL,
    PRIMARY KEY (ID)
);

ALTER TABLE CARPETA ADD CONSTRAINT carp_usua FOREIGN KEY (usuario_id) REFERENCES USUARIO(ID);
 
CREATE TABLE PERMISOS(
	ID int NOT NULL,
	nombre varchar(25) not null
	PRIMARY KEY(ID)
);
 

